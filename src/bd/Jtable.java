    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
     int fila = Tabla.getSelectedRow();
     if (fila == -1) {
         
         JOptionPane.showMessageDialog(null, "no se selecciono fila");
     } else {
         int cod = Integer.parseInt((String) Tabla.getValueAt(fila, 0) .toString());
         String Nombre = (String) Tabla.getValueAt(fila, 1);
         String Precio = (String) Tabla.getValueAt(fila, 2) .toString();
         String Cantidad = (String) Tabla.getValueAt(fila, 3);
         int Descripcion = Integer.parseInt ((String) Tabla.getValueAt (fila, 4) .toString());
         
         txtCod.setText(""+cod);
         txtNombre.setText(Nombre);
         txtPrecio.setText(Precio);
         txtcantidad.setText(Cantidad);
         txtDescripcion.setText(""+Descripcion);
         
     }
        
        
    }//GEN-LAST:event_TablaMouseClicked

