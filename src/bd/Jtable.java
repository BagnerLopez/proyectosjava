    void Modificar(){
        String cod = txtCod.getText();
    String nombre = txtNombre.getText();
    String Descripcion = txtDescripcion.getText();
    String precio = txtPrecio.getText();
    String cantidad = txtcantidad.getText();
    
    try{
        if (cod.equals("") || nombre.equals("") || Descripcion.equals("") ||  precio.equals("") || cantidad.equals("")){
            JOptionPane.showMessageDialog (null, "Falta Ingresar Datos");
            limpiarTabla();
        } else{
            
            String sql = "Update producto_joyeria set IDproducto_joyeria='" +cod+ "',nombre_producto='" +nombre+ "',descripcion_producto='"+Descripcion+"',precio='" +precio+ "',ID_categoria='" +cantidad+ "' where IDproducto_joyeria=" +cod;  
            
            conet = con1.conexion();
            st = conet.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog (null, "Datos del Producto Modificado");
            limpiarTabla();
        }
    }catch (Exception e){
        
    }
}
    
    
