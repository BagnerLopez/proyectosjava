    void consultar(){
        String sql = "SELECT * FROM producto_joyeria";
        try{
            conet = con1.conexion();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] producto_joyeria = new Object [5];
            modelo = (DefaultTableModel) Tabla.getModel();
            while (rs.next()){
                producto_joyeria [0] = rs.getInt ("IDproducto_joyeria");
                producto_joyeria [1] = rs.getString ("nombre_producto");
                producto_joyeria [2] = rs.getString ("descripcion_producto");
                producto_joyeria [3] = rs.getString ("precio");
                producto_joyeria [4] = rs.getInt ("ID_categoria");
                
                modelo.addRow(producto_joyeria);
            }
            Tabla.setModel(modelo);
            
        }catch (Exception e){
            
        }
    }
